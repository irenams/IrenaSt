void merge(int low, int mid, int high) {
		int l1, l2, i;
	
		for (l1 = low, l2 = mid + 1, i = low; l1 <= mid && l2 <= high; i++) {
			if (c[l1] <= c[l2])
				b[i] = c[l1++];
			else
				b[i] = c[l2++];
		}

		while (l1 <= mid)
			b[i++] = c[l1++];
	
		while (l2 <= high)
			b[i++] = c[l2++];
	
		for (i = low; i <= high; i++)
			c[i] = b[i];
}

void sort(int low, int high) {
	int mid;

	if (low < high) {
		mid = (low + high) / 2;
		sort(low, mid);
		sort(mid + 1, high);
		merge(low, mid, high);
	}
	else {
		return;
	}
}
